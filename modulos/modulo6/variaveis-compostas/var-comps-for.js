let vetor = [8, 1, 7, 4, 2, 9]; // Declaração do array

console.log(vetor[0]); // Mostra o valor do índice 0

// Estrutura de repetição for

for (let pos = 0; pos < vetor.length; pos++) {
  // Laço que percorre o array
  console.log(`A posição ${pos} tem o valor ${vetor[pos]}`); // Mostra a posição e o valor do índice
}
